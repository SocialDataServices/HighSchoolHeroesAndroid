package com.sds.hsh;

public class School {
	
	String id, name, city, state, zip, size;
	
	public School(String id, String name, String city, String state, String zip, String size) {
		this.id = id;
		this.name = name;
		this.city = city;
		this.state = state;
		this.zip = zip;
		this.size = size;
	}
}